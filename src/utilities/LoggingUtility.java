package utilities;

public class LoggingUtility {
    public static void printError(String errorText) {
        System.out.println("Error: " + errorText);
    }

    public static void printInfo(String infoText) {
        System.out.println("Info: " + infoText);
    }

    public static void printWarning(String warningText) {
        System.out.println("Warning: " + warningText);
    }
}
